	/**
	 * Dieses Objekt leitet alle Informationen zu Editierungen weiter an seine eigenen
	 * Beobachter
	 */
	@Override
	public void verarbeiteEditierung(EditierBefehl editierung) {
		log.finest(() -> "verarbeite " + editierung);
		informiere(editierung);
	}
	
