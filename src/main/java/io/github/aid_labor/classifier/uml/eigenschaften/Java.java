// public	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##
	
	static Java getInstanz() {
		if (singletonInstanz == null) {
			singletonInstanz = new Java();
		}
		
		return singletonInstanz;
	}
	
