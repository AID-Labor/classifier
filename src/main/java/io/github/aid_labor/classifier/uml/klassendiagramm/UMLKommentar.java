	@Override
	public int hashCode() {
		return Objects.hash(inhaltProperty);
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		UMLKommentar other = (UMLKommentar) obj;
		return Objects.equals(inhaltProperty, other.inhaltProperty);
	}
	
