	public UMLKommentar() {
		this.inhaltProperty = new JsonStringProperty();
	}
	
//	* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *
//  *	Getter und Setter																	*
