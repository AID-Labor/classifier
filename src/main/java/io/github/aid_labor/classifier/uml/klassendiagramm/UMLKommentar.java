// public	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##
	
	@Override
	public String getName() {
		return "";
	}
	
	public String getInhalt() {
		return inhaltProperty.get();
	}
	
	public void setInhalt(String inhalt) {
		this.inhaltProperty.set(inhalt);
	}
	
	public StringProperty getInhaltProperty() {
		return inhaltProperty;
	}
	
